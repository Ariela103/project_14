void printObjectFile(SingleWordObjFormat obj[], unsigned int ICF, unsigned int DCF)
{
    int i;
    printf("              %d   %d\n", ICF, DCF);
    for (i = 0; i < (DCF + ICF); i++)
        printf("%04d A%x-B%x-C%x-D%x-E%x\n", obj[i].Address, obj[i].A, obj[i].B, obj[i].C, obj[i].D, obj[i].E);
}